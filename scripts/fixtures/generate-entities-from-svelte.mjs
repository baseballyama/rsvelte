#!/usr/bin/env node

/**
 * Script to generate Rust code from Svelte's entities.js file.
 * This ensures complete compatibility with the Svelte compiler.
 *
 * Usage: node scripts/fixtures/generate-entities-from-svelte.mjs
 */

import { fileURLToPath } from "url";
import { dirname, join } from "path";
import { readFileSync, writeFileSync } from "fs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Read Svelte's entities.js
const entitiesPath = join(
  __dirname,
  "../../submodules/svelte/packages/svelte/src/compiler/phases/1-parse/utils/entities.js",
);
const entitiesModule = await import(entitiesPath);
const entities = entitiesModule.default;

// Convert entities object to sorted array
// Use a Map to avoid duplicates (prefer semicolon version if both exist)
const entityMap = new Map();
// Track entities that appear WITHOUT a semicolon in the source (legacy entities)
const legacyEntityMap = new Map();

for (const [name, codepoint] of Object.entries(entities)) {
  // Remove & prefix if present
  const cleanName = name.startsWith("&") ? name.slice(1) : name;
  // Remove ; suffix if present
  const finalName = cleanName.endsWith(";") ? cleanName.slice(0, -1) : cleanName;

  // Convert single codepoint to array for consistency
  const codepoints = Array.isArray(codepoint) ? codepoint : [codepoint];

  // Only add if not already present, or if this is a semicolon version
  if (!entityMap.has(finalName) || cleanName.endsWith(";")) {
    entityMap.set(finalName, codepoints);
  }

  // Track entities that explicitly appear without a semicolon in the source
  // These are the "legacy" entities that can be decoded without a trailing semicolon
  if (!cleanName.endsWith(";")) {
    legacyEntityMap.set(finalName, codepoints);
  }
}

// Convert map to array
const processed = Array.from(entityMap.entries()).map(([name, codepoints]) => ({
  name,
  codepoints,
}));

// Sort for binary search - use byte-wise comparison (same as Rust's Ord for &str)
processed.sort((a, b) => {
  if (a.name < b.name) return -1;
  if (a.name > b.name) return 1;
  return 0;
});

// Convert legacy (no-semicolon) entities to sorted array
const processedLegacy = Array.from(legacyEntityMap.entries()).map(([name, codepoints]) => ({
  name,
  codepoints,
}));
processedLegacy.sort((a, b) => {
  if (a.name < b.name) return -1;
  if (a.name > b.name) return 1;
  return 0;
});

// Generate Rust code
const rustCode = `//! Auto-generated HTML entity data from Svelte compiler.
//! Source: submodules/svelte/packages/svelte/src/compiler/phases/1-parse/utils/entities.js
//!
//! This file is generated by scripts/fixtures/generate-entities-from-svelte.mjs
//! DO NOT EDIT MANUALLY

#![allow(dead_code)]

/// Total number of HTML named character references
pub const ENTITY_COUNT: usize = ${processed.length};

/// HTML entity entry: (name, codepoints)
/// Names are sorted alphabetically for binary search
pub static ENTITIES: &[(&str, &[u32])] = &[
${processed
  .map(({ name, codepoints }) => {
    // Escape the name for Rust string literal
    const escapedName = name.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    const codepointStr = codepoints.join(", ");
    return `    ("${escapedName}", &[${codepointStr}]),`;
  })
  .join("\n")}
];

/// Legacy entities that can be decoded without a trailing semicolon.
/// These are the entities that appear without a semicolon in Svelte's entities.js source.
/// This is used for matching named entities when no semicolon is present in the HTML text.
/// Names are sorted alphabetically for binary search.
pub const LEGACY_ENTITY_COUNT: usize = ${processedLegacy.length};

pub static LEGACY_ENTITIES: &[(&str, &[u32])] = &[
${processedLegacy
  .map(({ name, codepoints }) => {
    const escapedName = name.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    const codepointStr = codepoints.join(", ");
    return `    ("${escapedName}", &[${codepointStr}]),`;
  })
  .join("\n")}
];

/// Decode a named HTML entity (without & prefix and ; suffix)
/// Returns the decoded character(s) as a String, or None if not found
pub fn decode_named_entity(name: &str) -> Option<String> {
    // Binary search for the entity
    match ENTITIES.binary_search_by(|(n, _)| (*n).cmp(name)) {
        Ok(idx) => {
            let (_, codepoints) = &ENTITIES[idx];
            let mut result = String::with_capacity(codepoints.len() * 4);
            for &cp in *codepoints {
                if let Some(c) = char::from_u32(cp) {
                    result.push(c);
                }
            }
            Some(result)
        }
        Err(_) => None,
    }
}

/// Decode a legacy named HTML entity (can match without a trailing semicolon)
/// Only matches entities that explicitly appear without semicolons in Svelte's entities.js.
/// Returns the decoded character(s) as a String, or None if not found
pub fn decode_legacy_named_entity(name: &str) -> Option<String> {
    // Binary search in the legacy entities table
    match LEGACY_ENTITIES.binary_search_by(|(n, _)| (*n).cmp(name)) {
        Ok(idx) => {
            let (_, codepoints) = &LEGACY_ENTITIES[idx];
            let mut result = String::with_capacity(codepoints.len() * 4);
            for &cp in *codepoints {
                if let Some(c) = char::from_u32(cp) {
                    result.push(c);
                }
            }
            Some(result)
        }
        Err(_) => None,
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_basic_entities() {
        assert_eq!(decode_named_entity("amp"), Some("&".to_string()));
        assert_eq!(decode_named_entity("lt"), Some("<".to_string()));
        assert_eq!(decode_named_entity("gt"), Some(">".to_string()));
        assert_eq!(decode_named_entity("quot"), Some("\\"".to_string()));
        assert_eq!(decode_named_entity("apos"), Some("'".to_string()));
        assert_eq!(decode_named_entity("nbsp"), Some("\\u{00A0}".to_string()));
    }

    #[test]
    fn test_extended_entities() {
        assert_eq!(decode_named_entity("copy"), Some("\\u{00A9}".to_string()));
        assert_eq!(decode_named_entity("reg"), Some("\\u{00AE}".to_string()));
        assert_eq!(decode_named_entity("trade"), Some("\\u{2122}".to_string()));
        assert_eq!(decode_named_entity("euro"), Some("\\u{20AC}".to_string()));
    }

    #[test]
    fn test_common_named_entities() {
        // Test some well-known named entities
        assert_eq!(decode_named_entity("euro"), Some("\\u{20AC}".to_string())); // €
        assert_eq!(decode_named_entity("trade"), Some("\\u{2122}".to_string())); // ™
        assert_eq!(decode_named_entity("hearts"), Some("\\u{2665}".to_string())); // ♥
        // nGt; is a single codepoint (≫, U+226B)
        assert_eq!(decode_named_entity("nGt"), Some("\\u{226B}".to_string()));
    }

    #[test]
    fn test_unknown_entity() {
        assert_eq!(decode_named_entity("notanentity"), None);
    }

    #[test]
    fn test_legacy_entities_without_semicolon() {
        // Svelte's entities.js includes some entities without semicolon suffix
        assert_eq!(decode_named_entity("AMP"), Some("&".to_string()));
        assert_eq!(decode_named_entity("LT"), Some("<".to_string()));
        assert_eq!(decode_named_entity("GT"), Some(">".to_string()));
        assert_eq!(decode_named_entity("QUOT"), Some("\\"".to_string()));
    }
}
`;

// Write to file
const outputPath = join(__dirname, "../../src/compiler/phases/1_parse/utils/entities_data.rs");
writeFileSync(outputPath, rustCode);

console.log(`✅ Generated ${outputPath}`);
console.log(`   Total entities: ${processed.length}`);
console.log(`   Legacy entities (no semicolon): ${processedLegacy.length}`);
