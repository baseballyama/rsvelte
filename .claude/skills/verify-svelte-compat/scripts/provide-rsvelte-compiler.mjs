#!/usr/bin/env node
// Build a local npm-style wrapper package for @rsvelte/compiler that re-exports
// the NAPI binding, then install it into a target via pnpm overrides.
//
// Usage:
//   node provide-rsvelte-compiler.mjs --target <path> --rsvelte-binding <abs-path-to-.node>
//
// Effect:
//   - creates compatibility/verify-svelte-compat/.cache/rsvelte-compiler-pkg/{package.json, index.cjs}
//   - merges a pnpm.overrides entry into <target>/package.json so installs
//     resolve @rsvelte/compiler and svelte/compiler against the local wrapper

import fs from 'node:fs';
import path from 'node:path';

const args = (() => {
  const out = {};
  for (let i = 2; i < process.argv.length; i++) {
    const k = process.argv[i];
    if (k.startsWith('--')) out[k.slice(2)] = process.argv[++i];
  }
  return out;
})();

const target = args.target;
const bindingPath = args['rsvelte-binding'];
if (!target || !bindingPath) {
  console.error('usage: provide-rsvelte-compiler.mjs --target <path> --rsvelte-binding <abs.node>');
  process.exit(64);
}

const targetAbs = path.resolve(target);
const bindingAbs = path.resolve(bindingPath);
const ROOT = path.resolve(import.meta.dirname, '../../../..');
const CACHE = path.join(ROOT, 'compatibility/verify-svelte-compat/.cache');
const PKG = path.join(CACHE, 'rsvelte-compiler-pkg');

fs.mkdirSync(PKG, { recursive: true });

const pkgJson = {
  name: '@rsvelte/compiler',
  version: '0.0.0-local',
  description: 'Local wrapper that re-exports rsvelte NAPI as @rsvelte/compiler.',
  main: './index.cjs',
  types: './index.d.ts',
  type: 'commonjs',
};
fs.writeFileSync(path.join(PKG, 'package.json'), JSON.stringify(pkgJson, null, 2));

fs.writeFileSync(
  path.join(PKG, 'index.cjs'),
  `// Auto-generated by verify-svelte-compat skill. Do not edit.
const rsvelte = require(${JSON.stringify(bindingAbs)});

module.exports = rsvelte;
module.exports.default = rsvelte;
`,
);

// Minimal type stub so type-checking targets do not explode.
fs.writeFileSync(
  path.join(PKG, 'index.d.ts'),
  `// Stub types for the local @rsvelte/compiler wrapper.
export const compile: (source: string, options?: any) => any;
export const compileModule: (source: string, options?: any) => any;
export const parse: (source: string, options?: any) => any;
export const preprocess: (source: string, preprocessors: any, options?: any) => any;
export const VERSION: string;
declare const _default: { compile: typeof compile; compileModule: typeof compileModule; parse: typeof parse; preprocess: typeof preprocess; VERSION: typeof VERSION };
export default _default;
`,
);

// Merge pnpm.overrides into target/package.json
const targetPkgPath = path.join(targetAbs, 'package.json');
const targetPkg = JSON.parse(fs.readFileSync(targetPkgPath, 'utf8'));
targetPkg.pnpm ??= {};
targetPkg.pnpm.overrides ??= {};
const fileSpec = `file:${path.relative(targetAbs, PKG)}`;
const before = JSON.stringify(targetPkg.pnpm.overrides);
targetPkg.pnpm.overrides['@rsvelte/compiler'] = fileSpec;
// Also override 'svelte' -> nothing (we want the official compiler comparison through the loader).
const after = JSON.stringify(targetPkg.pnpm.overrides);
if (before !== after) {
  fs.writeFileSync(targetPkgPath, JSON.stringify(targetPkg, null, 2) + '\n');
}

console.log(JSON.stringify({ ok: true, pkg: PKG, override: fileSpec }));
