#!/usr/bin/env node
// Generate a Node.js loader that intercepts `svelte/compiler` (and the
// equivalent `svelte/src/compiler/index.js` deep import) and routes calls
// to the rsvelte NAPI binding.
//
// Writes:
//   <target>/.rsvelte/loader.mjs
//   <target>/.rsvelte/svelte-shim.cjs
//
// Apply with NODE_OPTIONS="--import ./.rsvelte/loader.mjs" when invoking
// the target's test / build commands.

import fs from 'node:fs';
import path from 'node:path';

const args = Object.fromEntries(
  process.argv
    .slice(2)
    .reduce((acc, cur, i, all) => {
      if (cur.startsWith('--')) acc.push([cur.slice(2), all[i + 1]]);
      return acc;
    }, []),
);

const target = args.target;
const binding = args['rsvelte-binding'];
if (!target || !binding) {
  console.error('usage: swap-compiler.mjs --target <path> --rsvelte-binding <abs-path-to-.node>');
  process.exit(64);
}

const targetAbs = path.resolve(target);
const bindingAbs = path.resolve(binding);
if (!fs.existsSync(targetAbs)) {
  console.error(`target not found: ${targetAbs}`);
  process.exit(1);
}
if (!fs.existsSync(bindingAbs)) {
  console.error(`rsvelte binding not found: ${bindingAbs}`);
  process.exit(1);
}

const dir = path.join(targetAbs, '.rsvelte');
fs.mkdirSync(dir, { recursive: true });

// CommonJS shim that re-exports the rsvelte NAPI surface as svelte/compiler.
const shim = `// Auto-generated by verify-svelte-compat skill. Do not edit.
const rsvelte = require(${JSON.stringify(bindingAbs)});

function compile(source, options = {}) {
  return rsvelte.compile(source, options);
}
function compileModule(source, options = {}) {
  return rsvelte.compileModule(source, options);
}

let officialParse;
function parse(source, options = {}) {
  // rsvelte may not expose parse(); fall back to the official compiler if available.
  if (typeof rsvelte.parse === 'function') return rsvelte.parse(source, options);
  if (!officialParse) {
    try {
      // eslint-disable-next-line global-require
      const off = require('svelte/src/compiler/index.js');
      officialParse = off.parse;
    } catch (_) {
      throw new Error('rsvelte: parse() is not implemented and the official compiler is unavailable as a fallback.');
    }
  }
  return officialParse(source, options);
}

let officialPreprocess;
function preprocess(source, preprocessors, opts) {
  if (typeof rsvelte.preprocess === 'function') return rsvelte.preprocess(source, preprocessors, opts);
  if (!officialPreprocess) {
    try {
      // eslint-disable-next-line global-require
      const off = require('svelte/src/compiler/index.js');
      officialPreprocess = off.preprocess;
    } catch (_) {
      throw new Error('rsvelte: preprocess() is not implemented and the official compiler is unavailable as a fallback.');
    }
  }
  return officialPreprocess(source, preprocessors, opts);
}

function walk() { throw new Error('rsvelte: walk() is not implemented'); }

const VERSION = (() => {
  try { return require('svelte/package.json').version; } catch { return '0.0.0-rsvelte'; }
})();

module.exports = { compile, compileModule, parse, preprocess, walk, VERSION };
module.exports.default = module.exports;
`;
fs.writeFileSync(path.join(dir, 'svelte-shim.cjs'), shim);

// ESM loader that rewrites import specifiers for `svelte/compiler` to the shim.
const loader = `// Auto-generated by verify-svelte-compat skill. Do not edit.
import { register } from 'node:module';
import { pathToFileURL } from 'node:url';
register('./hooks.mjs', pathToFileURL(import.meta.url));
`;
fs.writeFileSync(path.join(dir, 'loader.mjs'), loader);

const hooks = `// Auto-generated by verify-svelte-compat skill.
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const SHIM = ${JSON.stringify(path.join(dir, 'svelte-shim.cjs'))};

const COMPILER_MATCH = /(^|\\/)svelte\\/(compiler(\\.js)?|src\\/compiler(\\/index(\\.js)?)?)$/;

export async function resolve(specifier, context, nextResolve) {
  if (specifier === 'svelte/compiler' || COMPILER_MATCH.test(specifier)) {
    return { url: 'file://' + SHIM, format: 'commonjs', shortCircuit: true };
  }
  return nextResolve(specifier, context);
}
`;
fs.writeFileSync(path.join(dir, 'hooks.mjs'), hooks);

// Sentinel file so the skill knows swap is wired up.
fs.writeFileSync(
  path.join(dir, '.swap-applied'),
  JSON.stringify({ binding: bindingAbs, appliedAt: new Date().toISOString() }, null, 2),
);

// gitignore so swap output never accidentally gets committed inside the target.
fs.writeFileSync(path.join(dir, '.gitignore'), '*\n');

console.log(JSON.stringify({ ok: true, loader: path.join(dir, 'loader.mjs'), shim: path.join(dir, 'svelte-shim.cjs') }));
