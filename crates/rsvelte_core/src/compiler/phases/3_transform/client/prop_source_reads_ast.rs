//! AST-based rewrite of `propName` reads to `propName()` prop-getter
//! calls in legacy (non-runes) mode component bodies.
//!
//! Replaces `props_transforms::wrap_prop_source_reads`. The text
//! version walks the input char-by-char tracking string/comment
//! state and applies nine sequential heuristic guards
//! (`preceded_by_dot`, `in_param_position`, `is_assignment_target`,
//! `is_getter_setter_name`, `is_property_key`, `is_shadowed`,
//! `is_inside_update_call`, `is_member_mutation`,
//! `is_sole_derived_arg`). An earlier AST attempt
//! (`perf/prop-source-reads-ast`, reverted) regressed 26 runtime
//! fixtures because hand-rolled `is_shadowed_by_function_param` /
//! `is_shadowed_by_local_var_decl` weren't reproduced — that's
//! exactly what `oxc_semantic` now gives us (PR #215). Six of the
//! other guards are natural for an AST visitor; the remaining
//! three (`update_call` / `derived_sole_arg` / `member_mutation`
//! base / shorthand expansion) are handled via parent-context
//! tracking on `visit_call_expression`, `visit_assignment_*` and
//! `visit_object_property`.
//!
//! ## Mapping (preserved exactly vs text version)
//!
//! | Source             | Replacement       | Notes                                |
//! |--------------------|-------------------|--------------------------------------|
//! | `count`            | `count()`         | bare read                            |
//! | `{ count }`        | `{ count: count() }` | shorthand expansion                 |
//! | `count = expr`     | `count = expr`    | LHS skipped (handled downstream)     |
//! | `count++`          | `count++`         | UpdateExpression arg skipped         |
//! | `obj.count`        | `obj.count`       | property side never visited          |
//! | `{ count: 1 }`     | `{ count: 1 }`    | property key never visited           |
//! | `function f(count) { count }` | unchanged | shadowed by function param   |
//! | `let count; count` | unchanged         | shadowed by local declaration        |
//! | `$.update_prop(count)` | unchanged     | first arg of these calls skipped     |
//! | `$.update_pre_prop(count)` | unchanged | (same)                               |
//! | `$.derived(count)` | unchanged         | sole-arg of `$.derived` skipped      |
//! | `count[i] = 1`     | `count[i] = 1`    | bindable prop member mutation LHS    |
//! | `count.x = 1`      | `count.x = 1`    | (same)                               |
//! | `count[i] = 1` (non-bindable) | `count()[i] = 1` | non-bindable still wraps  |
//!
//! ## Return shape
//!
//! Returns `Some(rewritten)` when at least one position was wrapped
//! or expanded. Returns `None` if:
//! - The source contains a prop-declaration marker (`$.prop(` /
//!   `$.prop_source(` / `$.rest_props(`) — these lines are
//!   generated by `transform_props_destructuring` and the text
//!   version leaves them untouched.
//! - `prop_vars` is empty.
//! - No `prop_var` substring appears in the source.
//! - The source fails to parse.
//! - Nothing matched the wrap criteria.
//!
//! Callers fall back to the text predecessor on `None` so a parse
//! failure or "nothing to do" both preserve current behavior.

