// ============================================================================
// Helper to run and print test category
// ============================================================================

fn run_and_print<F>(name: &str, runner: F) -> Category
where
    F: FnOnce() -> Category,
{
    eprint!("  Running {} tests... ", name);
    io::stderr().flush().unwrap();
    let result = runner();
    if result.skipped == result.total {
        eprintln!("all {} skipped", result.total);
    } else if result.skipped > 0 {
        eprintln!(
            "{}/{} passed ({} skipped)",
            result.passed,
            result.total - result.skipped,
            result.skipped
        );
    } else {
        eprintln!("{}/{} passed", result.passed, result.total);
    }
    result
}

