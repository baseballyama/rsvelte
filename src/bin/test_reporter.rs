// ============================================================================

#[derive(Debug, Deserialize)]
#[allow(dead_code)]
struct ValidatorError {
    code: String,
    message: String,
}

fn run_validator_tests() -> Category {
    let samples = get_samples("validator");

    let mut tests = Vec::new();
    let mut passed = 0;
    let mut failed = 0;
    let mut skipped = 0;

    for sample_dir in &samples {
        let test_name = sample_dir
            .file_name()
            .and_then(|n| n.to_str())
            .unwrap_or("unknown")
            .to_string();

        let svelte_path = sample_dir.join("input.svelte");
        let module_path = sample_dir.join("input.svelte.js");
        let warnings_path = sample_dir.join("warnings.json");
        let errors_path = sample_dir.join("errors.json");

        // Skip module tests
        if module_path.exists() && !svelte_path.exists() {
            skipped += 1;
            tests.push(TestCase {
                name: test_name,
                status: "skip".to_string(),
                error_message: None,
                skip_reason: Some("Module compilation not implemented".to_string()),
            });
            continue;
        }

        if !svelte_path.exists() {
            continue;
        }

        let input = match fs::read_to_string(&svelte_path) {
            Ok(s) => s,
            Err(_) => continue,
        };

        let expected_warnings: Vec<serde_json::Value> = if warnings_path.exists() {
            let content = fs::read_to_string(&warnings_path).unwrap_or_default();
            serde_json::from_str(&content).unwrap_or_default()
        } else {
            Vec::new()
        };

        let expected_error: Option<ValidatorError> = if errors_path.exists() {
            let content = fs::read_to_string(&errors_path).unwrap_or_default();
            let errors: Vec<ValidatorError> = serde_json::from_str(&content).unwrap_or_default();
            errors.into_iter().next()
        } else {
            None
        };

        let result = std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| {
            let options = CompileOptions {
                generate: GenerateMode::Client,
                filename: Some(format!("{}/input.svelte", test_name)),
                ..Default::default()
            };
            compile(&input, options)
        }));

        match result {
            Err(_) => {
                failed += 1;
                tests.push(TestCase {
                    name: test_name,
                    status: "fail".to_string(),
                    error_message: Some("Compilation panicked".to_string()),
                    skip_reason: None,
                });
            }
            Ok(compile_result) => match compile_result {
                Ok(result) => {
                    if let Some(expected_error) = &expected_error {
                        failed += 1;
                        tests.push(TestCase {
                            name: test_name,
                            status: "fail".to_string(),
                            error_message: Some(format!(
                                "Expected error '{}' but compilation succeeded",
                                expected_error.code
                            )),
                            skip_reason: None,
                        });
                    } else {
                        // Check warnings count
                        if result.warnings.len() == expected_warnings.len() {
                            passed += 1;
                            tests.push(TestCase {
                                name: test_name,
                                status: "pass".to_string(),
                                error_message: None,
                                skip_reason: None,
                            });
                        } else {
                            failed += 1;
                            tests.push(TestCase {
                                name: test_name,
                                status: "fail".to_string(),
                                error_message: Some(format!(
                                    "Expected {} warnings, got {}",
                                    expected_warnings.len(),
                                    result.warnings.len()
                                )),
                                skip_reason: None,
                            });
                        }
                    }
                }
                Err(e) => {
                    if let Some(expected_error) = &expected_error {
                        let error_str = format!("{:?}", e);
                        let code_matches = error_str.contains(&expected_error.code)
                            || error_str
                                .to_lowercase()
                                .contains(&expected_error.code.replace('_', " ").to_lowercase());

                        if code_matches {
                            passed += 1;
                            tests.push(TestCase {
                                name: test_name,
                                status: "pass".to_string(),
                                error_message: None,
                                skip_reason: None,
                            });
                        } else {
                            failed += 1;
                            tests.push(TestCase {
                                name: test_name,
                                status: "fail".to_string(),
                                error_message: Some(format!(
                                    "Expected error '{}', got: {}",
                                    expected_error.code, error_str
                                )),
                                skip_reason: None,
                            });
                        }
                    } else {
                        failed += 1;
                        tests.push(TestCase {
                            name: test_name,
                            status: "fail".to_string(),
                            error_message: Some(format!("Unexpected compilation error: {:?}", e)),
                            skip_reason: None,
                        });
                    }
                }
            },
        }
    }

    let total = passed + failed + skipped;
    let percentage = if total - skipped > 0 {
        (passed as f64 / (total - skipped) as f64) * 100.0
    } else {
        0.0
    };

    Category {
        id: "validator".to_string(),
        name: "Validator".to_string(),
        total,
        passed,
        failed,
        skipped,
        percentage,
        tests,
    }
}

// ============================================================================
// Runtime Tests (compare JS output with expected)
// ============================================================================

fn run_runtime_tests(
    test_type: &str,
    display_name: &str,
    expected_outputs: &serde_json::Value,
) -> Category {
    let id = test_type.replace('-', "_");
    let samples = get_samples(test_type);

    let mut tests = Vec::new();
    let mut passed = 0;
    let mut failed = 0;
    let mut skipped = 0;

    for sample_dir in &samples {
        let test_name = sample_dir
            .file_name()
            .and_then(|n| n.to_str())
            .unwrap_or("unknown")
            .to_string();

        let main_path = sample_dir.join("main.svelte");
        let alt_main_path = sample_dir.join("_main.svelte");
        let input_path = if main_path.exists() {
            main_path
        } else if alt_main_path.exists() {
            alt_main_path
        } else {
            continue;
        };

        // Get expected output
        let expected_client =
            get_expected_output(expected_outputs, test_type, &test_name, "client");
        let expected_server =
            get_expected_output(expected_outputs, test_type, &test_name, "server");

        if expected_client.is_none() && expected_server.is_none() {
            skipped += 1;
            tests.push(TestCase {
                name: test_name,
                status: "skip".to_string(),
                error_message: None,
                skip_reason: Some("No expected output available".to_string()),
            });
            continue;
        }

        let input = match fs::read_to_string(&input_path) {
            Ok(s) => s,
            Err(_) => continue,
        };

        // Compile with timeout
        let input_clone = input.clone();
        let name_clone = test_name.clone();

        let (tx, rx) = std::sync::mpsc::channel();
        std::thread::spawn(move || {
            let result = std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| {
                let client_options = CompileOptions {
                    generate: GenerateMode::Client,
                    filename: Some(format!("{}/main.svelte", name_clone)),
                    ..Default::default()
                };
                let server_options = CompileOptions {
                    generate: GenerateMode::Server,
                    filename: Some(format!("{}/main.svelte", name_clone)),
                    ..Default::default()
                };
                (
                    compile(&input_clone, client_options),
                    compile(&input_clone, server_options),
                )
            }));
            let _ = tx.send(result);
        });

        let result = match rx.recv_timeout(std::time::Duration::from_secs(5)) {
            Ok(r) => r,
            Err(_) => {
                failed += 1;
                tests.push(TestCase {
                    name: test_name,
                    status: "fail".to_string(),
                    error_message: Some("Test timed out".to_string()),
                    skip_reason: None,
                });
                continue;
            }
        };

        match result {
            Err(_) => {
                failed += 1;
                tests.push(TestCase {
                    name: test_name,
                    status: "fail".to_string(),
                    error_message: Some("Compilation panicked".to_string()),
                    skip_reason: None,
                });
            }
            Ok((client_result, server_result)) => {
                let mut client_ok = true;
                let mut server_ok = true;
                let mut error_msg = String::new();

                // Compare client output
                if let Some(expected) = expected_client {
                    match &client_result {
                        Ok(result) => {
                            if !compare_js(&result.js.code, expected) {
                                client_ok = false;
                                error_msg = "Client JS mismatch".to_string();
                            }
                        }
                        Err(e) => {
                            client_ok = false;
                            error_msg = format!("Client compilation error: {}", e);
                        }
                    }
                }

                // Compare server output
                if let Some(expected) = expected_server {
                    match &server_result {
                        Ok(result) => {
                            if !compare_js(&result.js.code, expected) {
                                server_ok = false;
                                if error_msg.is_empty() {
                                    error_msg = "Server JS mismatch".to_string();
                                }
                            }
                        }
                        Err(e) => {
                            server_ok = false;
                            if error_msg.is_empty() {
                                error_msg = format!("Server compilation error: {}", e);
                            }
                        }
                    }
                }

                if client_ok && server_ok {
                    passed += 1;
                    tests.push(TestCase {
                        name: test_name,
                        status: "pass".to_string(),
                        error_message: None,
                        skip_reason: None,
                    });
                } else {
                    failed += 1;
                    tests.push(TestCase {
                        name: test_name,
                        status: "fail".to_string(),
                        error_message: Some(error_msg),
                        skip_reason: None,
                    });
                }
            }
        }
    }

    let total = passed + failed + skipped;
    let percentage = if total - skipped > 0 {
        (passed as f64 / (total - skipped) as f64) * 100.0
    } else {
        0.0
    };

    Category {
        id,
        name: display_name.to_string(),
        total,
        passed,
        failed,
        skipped,
        percentage,
        tests,
    }
}

