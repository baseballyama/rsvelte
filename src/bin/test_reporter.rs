// ============================================================================
// JSON Schema Types
// ============================================================================

#[derive(Serialize)]
struct TestResults {
    generated_at: String,
    commit_sha: String,
    summary: Summary,
    categories: Vec<Category>,
}

#[derive(Serialize)]
struct Summary {
    total: usize,
    passed: usize,
    failed: usize,
    skipped: usize,
    percentage: f64,
}

#[derive(Serialize)]
struct Category {
    id: String,
    name: String,
    total: usize,
    passed: usize,
    failed: usize,
    skipped: usize,
    percentage: f64,
    tests: Vec<TestCase>,
}

#[derive(Serialize)]
struct TestCase {
    name: String,
    status: String, // "pass", "fail", "skip"
    #[serde(skip_serializing_if = "Option::is_none")]
    error_message: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none")]
    skip_reason: Option<String>,
}

// ============================================================================
// Common Utilities
// ============================================================================

fn svelte_path() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("svelte")
}

fn get_commit_sha() -> String {
    Command::new("git")
        .args(["rev-parse", "--short", "HEAD"])
        .output()
        .ok()
        .and_then(|o| String::from_utf8(o.stdout).ok())
        .map(|s| s.trim().to_string())
        .unwrap_or_else(|| "unknown".to_string())
}

fn get_samples(test_dir: &str) -> Vec<PathBuf> {
    let samples_dir = svelte_path()
        .join("packages/svelte/tests")
        .join(test_dir)
        .join("samples");

    if !samples_dir.exists() {
        return Vec::new();
    }

    let mut samples: Vec<PathBuf> = WalkDir::new(&samples_dir)
        .min_depth(1)
        .max_depth(1)
        .into_iter()
        .filter_map(|e| e.ok())
        .filter(|e| e.file_type().is_dir())
        .map(|e| e.path().to_path_buf())
        .collect();

    samples.sort();
    samples
}

// Load expected outputs from JSON file generated by Svelte compiler
fn load_expected_outputs() -> serde_json::Value {
    let expected_path =
        PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("docs/static/expected-outputs.json");

    if !expected_path.exists() {
        eprintln!(
            "Warning: expected-outputs.json not found. Run 'node scripts/generate-expected-outputs.mjs > docs/static/expected-outputs.json' first."
        );
        return serde_json::Value::Null;
    }

    let content = fs::read_to_string(&expected_path).unwrap_or_default();
    serde_json::from_str(&content).unwrap_or(serde_json::Value::Null)
}

// Get expected output for a specific test
fn get_expected_output<'a>(
    expected: &'a serde_json::Value,
    category: &str,
    test_name: &str,
    mode: &str,
) -> Option<&'a str> {
    expected
        .get(category)?
        .get(test_name)?
        .get(mode)?
        .get("js")?
        .as_str()
}

