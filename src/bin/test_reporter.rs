fn run_parser_tests(test_type: &str, modern: bool) -> Category {
    let id = test_type.replace('-', "_");
    let name = if modern {
        "Parser Modern"
    } else {
        "Parser Legacy"
    };
    let samples = get_samples(test_type);

    let mut tests = Vec::new();
    let mut passed = 0;
    let mut failed = 0;
    let mut skipped = 0;

    for sample_dir in &samples {
        let test_name = sample_dir
            .file_name()
            .and_then(|n| n.to_str())
            .unwrap_or("unknown")
            .to_string();

        let input_path = sample_dir.join("input.svelte");
        let output_path = sample_dir.join("output.json");

        if !input_path.exists() || !output_path.exists() {
            skipped += 1;
            tests.push(TestCase {
                name: test_name,
                status: "skip".to_string(),
                error_message: None,
                skip_reason: Some("missing input or output fixture".to_string()),
            });
            continue;
        }

        let input = match fs::read_to_string(&input_path) {
            Ok(s) => s,
            Err(e) => {
                skipped += 1;
                tests.push(TestCase {
                    name: test_name,
                    status: "skip".to_string(),
                    error_message: None,
                    skip_reason: Some(format!("failed to read input: {}", e)),
                });
                continue;
            }
        };

        let options = ParseOptions {
            modern,
            loose: false,
            ..Default::default()
        };

        let result = parse(&input, options);

        match result {
            Ok(ast) => {
                let expected = fs::read_to_string(&output_path).unwrap_or_default();
                let actual_json = format!("{:?}", ast);

                let actual_normalized = normalize_json(&actual_json);
                let expected_normalized = normalize_json(&expected);

                if actual_normalized == expected_normalized {
                    passed += 1;
                    tests.push(TestCase {
                        name: test_name,
                        status: "pass".to_string(),
                        error_message: None,
                        skip_reason: None,
                    });
                } else {
                    failed += 1;
                    tests.push(TestCase {
                        name: test_name,
                        status: "fail".to_string(),
                        error_message: Some("AST mismatch".to_string()),
                        skip_reason: None,
                    });
                }
            }
            Err(e) => {
                failed += 1;
                tests.push(TestCase {
                    name: test_name,
                    status: "fail".to_string(),
                    error_message: Some(format!("Parse error: {:?}", e)),
                    skip_reason: None,
                });
            }
        }
    }

    let total = passed + failed + skipped;
    let percentage = if total - skipped > 0 {
        (passed as f64 / (total - skipped) as f64) * 100.0
    } else {
        0.0
    };

    Category {
        id,
        name: name.to_string(),
        total,
        passed,
        failed,
        skipped,
        percentage,
        tests,
    }
}

fn normalize_json(json: &str) -> serde_json::Value {
    let mut value: serde_json::Value =
        serde_json::from_str(json).unwrap_or(serde_json::Value::Null);
    remove_internal_fields(&mut value);
    value
}

fn remove_internal_fields(value: &mut serde_json::Value) {
    match value {
        serde_json::Value::Object(map) => {
            map.remove("metadata");

            fn remove_character_from_loc(loc: &mut serde_json::Value) {
                if let serde_json::Value::Object(loc_map) = loc {
                    if let Some(serde_json::Value::Object(start)) = loc_map.get_mut("start") {
                        start.remove("character");
                    }
                    if let Some(serde_json::Value::Object(end)) = loc_map.get_mut("end") {
                        end.remove("character");
                    }
                }
            }

            if let Some(loc) = map.get_mut("loc") {
                remove_character_from_loc(loc);
            }

            if let Some(name_loc) = map.get_mut("name_loc") {
                remove_character_from_loc(name_loc);
            }

            for (_, v) in map.iter_mut() {
                remove_internal_fields(v);
            }
        }
        serde_json::Value::Array(arr) => {
            for v in arr.iter_mut() {
                remove_internal_fields(v);
            }
        }
        _ => {}
    }
}

