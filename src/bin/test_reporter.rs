fn run_snapshot_tests() -> Category {
    let samples = get_samples("snapshot");

    let mut tests = Vec::new();
    let mut passed = 0;
    let mut failed = 0;
    let mut skipped = 0;

    for sample_dir in &samples {
        let test_name = sample_dir
            .file_name()
            .and_then(|n| n.to_str())
            .unwrap_or("unknown")
            .to_string();

        let input_path = sample_dir.join("index.svelte");
        if !input_path.exists() {
            continue;
        }

        // Check for unsupported options
        let config_path = sample_dir.join("_config.js");
        if let Ok(config) = fs::read_to_string(&config_path)
            && (config.contains("async: true")
                || config.contains("hmr: true")
                || config.contains("fragments:"))
        {
            skipped += 1;
            tests.push(TestCase {
                name: test_name,
                status: "skip".to_string(),
                error_message: None,
                skip_reason: Some("Requires unsupported compile options".to_string()),
            });
            continue;
        }

        let expected_client = sample_dir.join("_expected/client/index.svelte.js");
        let expected_server = sample_dir.join("_expected/server/index.svelte.js");

        if !expected_client.exists() && !expected_server.exists() {
            continue;
        }

        let input = match fs::read_to_string(&input_path) {
            Ok(s) => s,
            Err(_) => continue,
        };

        let mut client_ok = true;
        let mut server_ok = true;
        let mut error_msg = String::new();

        // Test client
        if expected_client.exists() {
            let expected = fs::read_to_string(&expected_client).unwrap_or_default();
            let options = CompileOptions {
                generate: GenerateMode::Client,
                ..Default::default()
            };

            match compile(&input, options) {
                Ok(result) => {
                    if !compare_js(&result.js.code, &expected) {
                        client_ok = false;
                        error_msg = "Client JS mismatch".to_string();
                    }
                }
                Err(e) => {
                    client_ok = false;
                    error_msg = format!("Client compilation error: {}", e);
                }
            }
        }

        // Test server
        if expected_server.exists() {
            let expected = fs::read_to_string(&expected_server).unwrap_or_default();
            let options = CompileOptions {
                generate: GenerateMode::Server,
                ..Default::default()
            };

            match compile(&input, options) {
                Ok(result) => {
                    if !compare_js(&result.js.code, &expected) {
                        server_ok = false;
                        if error_msg.is_empty() {
                            error_msg = "Server JS mismatch".to_string();
                        }
                    }
                }
                Err(e) => {
                    server_ok = false;
                    if error_msg.is_empty() {
                        error_msg = format!("Server compilation error: {}", e);
                    }
                }
            }
        }

        if client_ok && server_ok {
            passed += 1;
            tests.push(TestCase {
                name: test_name,
                status: "pass".to_string(),
                error_message: None,
                skip_reason: None,
            });
        } else {
            failed += 1;
            tests.push(TestCase {
                name: test_name,
                status: "fail".to_string(),
                error_message: Some(error_msg),
                skip_reason: None,
            });
        }
    }

    let total = passed + failed + skipped;
    let percentage = if total - skipped > 0 {
        (passed as f64 / (total - skipped) as f64) * 100.0
    } else {
        0.0
    };

    Category {
        id: "snapshot".to_string(),
        name: "Compiler Snapshot".to_string(),
        total,
        passed,
        failed,
        skipped,
        percentage,
        tests,
    }
}

fn normalize_js(js: &str) -> String {
    let js = normalize_quotes(js);
    let js = collapse_multiline_constructs(&js);

    js.lines()
        .filter(|line| !line.trim().is_empty())
        .map(|line| line.trim_end())
        .map(normalize_spacing)
        .collect::<Vec<_>>()
        .join("\n")
}

fn normalize_quotes(js: &str) -> String {
    let mut result = String::new();
    let chars: Vec<char> = js.chars().collect();
    let mut i = 0;

    while i < chars.len() {
        let c = chars[i];
        if c == '"' && (i == 0 || chars[i - 1] != '\\') {
            result.push('\'');
        } else {
            result.push(c);
        }
        i += 1;
    }

    result
}

fn collapse_multiline_constructs(js: &str) -> String {
    let mut result = String::new();
    let mut depth = 0;
    let mut in_template = false;
    let mut in_string = false;
    let mut string_char = ' ';
    let chars: Vec<char> = js.chars().collect();
    let mut i = 0;

    while i < chars.len() {
        let c = chars[i];

        if !in_string && c == '`' && (i == 0 || chars[i - 1] != '\\') {
            in_template = !in_template;
        }
        if !in_template && (c == '\'' || c == '"') && (i == 0 || chars[i - 1] != '\\') {
            if !in_string {
                in_string = true;
                string_char = c;
            } else if c == string_char {
                in_string = false;
            }
        }

        if !in_string && !in_template {
            if c == '[' || c == '{' {
                depth += 1;
            } else if c == ']' || c == '}' {
                depth -= 1;
            }
        }

        if (c == '\n' || c == '\r') && depth > 0 && !in_template {
            while i + 1 < chars.len()
                && (chars[i + 1] == ' '
                    || chars[i + 1] == '\t'
                    || chars[i + 1] == '\n'
                    || chars[i + 1] == '\r')
            {
                i += 1;
            }
            let last_char = result.chars().last();
            let next_char = chars.get(i + 1);
            if last_char != Some('[')
                && last_char != Some('{')
                && next_char != Some(&']')
                && next_char != Some(&'}')
            {
                result.push(' ');
            }
        } else {
            result.push(c);
        }
        i += 1;
    }

    result
}

fn normalize_spacing(line: &str) -> String {
    let line = line.replace(",...", ", ...");
    let mut result = String::new();
    let mut last_was_space = false;
    for c in line.chars() {
        if c == ' ' {
            if !last_was_space {
                result.push(c);
            }
            last_was_space = true;
        } else {
            result.push(c);
            last_was_space = false;
        }
    }
    result
}

fn compare_js(actual: &str, expected: &str) -> bool {
    normalize_js(actual) == normalize_js(expected)
}

