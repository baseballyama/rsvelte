fn run_css_tests() -> Category {
    let samples = get_samples("css");

    let mut tests = Vec::new();
    let mut passed = 0;
    let mut failed = 0;
    let mut skipped = 0;

    for sample_dir in &samples {
        let test_name = sample_dir
            .file_name()
            .and_then(|n| n.to_str())
            .unwrap_or("unknown")
            .to_string();

        let input_path = sample_dir.join("input.svelte");
        let expected_css_path = sample_dir.join("expected.css");

        if !input_path.exists() {
            skipped += 1;
            tests.push(TestCase {
                name: test_name,
                status: "skip".to_string(),
                error_message: None,
                skip_reason: Some("missing input fixture".to_string()),
            });
            continue;
        }

        let input = match fs::read_to_string(&input_path) {
            Ok(s) => s,
            Err(e) => {
                skipped += 1;
                tests.push(TestCase {
                    name: test_name,
                    status: "skip".to_string(),
                    error_message: None,
                    skip_reason: Some(format!("failed to read input: {}", e)),
                });
                continue;
            }
        };

        // Run with timeout using a channel
        let input_clone = input.clone();
        let name_clone = test_name.clone();

        let (tx, rx) = std::sync::mpsc::channel();
        std::thread::spawn(move || {
            let result = std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| {
                let options = CompileOptions {
                    generate: GenerateMode::Client,
                    filename: Some(format!("{}/input.svelte", name_clone)),
                    css: CssMode::External,
                    ..Default::default()
                };
                compile(&input_clone, options)
            }));
            let _ = tx.send(result);
        });

        let result = match rx.recv_timeout(std::time::Duration::from_secs(5)) {
            Ok(r) => r,
            Err(_) => {
                failed += 1;
                tests.push(TestCase {
                    name: test_name,
                    status: "fail".to_string(),
                    error_message: Some("Test timed out".to_string()),
                    skip_reason: None,
                });
                continue;
            }
        };

        match result {
            Err(_) => {
                failed += 1;
                tests.push(TestCase {
                    name: test_name,
                    status: "fail".to_string(),
                    error_message: Some("Compilation panicked".to_string()),
                    skip_reason: None,
                });
            }
            Ok(compile_result) => match compile_result {
                Ok(result) => {
                    let actual_css = result.css.map(|c| c.code).unwrap_or_default();

                    if expected_css_path.exists() {
                        let expected_css =
                            fs::read_to_string(&expected_css_path).unwrap_or_default();
                        let actual_normalized = normalize_css(&actual_css);
                        let expected_normalized = normalize_css(&expected_css);

                        if actual_normalized == expected_normalized {
                            passed += 1;
                            tests.push(TestCase {
                                name: test_name,
                                status: "pass".to_string(),
                                error_message: None,
                                skip_reason: None,
                            });
                        } else {
                            failed += 1;
                            tests.push(TestCase {
                                name: test_name,
                                status: "fail".to_string(),
                                error_message: Some("CSS mismatch".to_string()),
                                skip_reason: None,
                            });
                        }
                    } else {
                        // No expected CSS, just check compilation
                        passed += 1;
                        tests.push(TestCase {
                            name: test_name,
                            status: "pass".to_string(),
                            error_message: None,
                            skip_reason: None,
                        });
                    }
                }
                Err(e) => {
                    failed += 1;
                    tests.push(TestCase {
                        name: test_name,
                        status: "fail".to_string(),
                        error_message: Some(format!("Compilation error: {:?}", e)),
                        skip_reason: None,
                    });
                }
            },
        }
    }

    let total = passed + failed + skipped;
    let percentage = if total - skipped > 0 {
        (passed as f64 / (total - skipped) as f64) * 100.0
    } else {
        0.0
    };

    Category {
        id: "css".to_string(),
        name: "CSS".to_string(),
        total,
        passed,
        failed,
        skipped,
        percentage,
        tests,
    }
}

fn normalize_css(css: &str) -> String {
    let hash_re = regex::Regex::new(r"svelte-[a-z0-9]+").unwrap();
    let normalized = hash_re.replace_all(css, "svelte-xyz");

    normalized
        .lines()
        .map(|line| line.trim())
        .filter(|line| !line.is_empty())
        .collect::<Vec<_>>()
        .join("\n")
}

